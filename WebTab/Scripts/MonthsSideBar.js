function openNav() {
    document.getElementById("monthsSideNav").style.width = "200px";
    document.getElementById("main").style.marginLeft = "200px";
    document.getElementById("navMenu").style.marginLeft = "200px";
}

function closeNav() {
    document.getElementById("monthsSideNav").style.width = "0";
    document.getElementById("main").style.marginLeft = "0";
    document.getElementById("navMenu").style.marginLeft = "0";
}